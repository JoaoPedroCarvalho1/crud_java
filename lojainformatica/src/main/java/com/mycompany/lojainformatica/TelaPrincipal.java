    private void BtnAlterarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_BtnAlterarActionPerformed
        DefaultTableModel modeloProduto = (DefaultTableModel) TblProdutos.getModel();

        int linhaSelecionada = TblProdutos.getSelectedRow();

        if (linhaSelecionada >= 0) {
            int id = Integer.parseInt(modeloProduto.getValueAt(linhaSelecionada, 0).toString());
            String hd = modeloProduto.getValueAt(linhaSelecionada, 2).toString();
            String processador = modeloProduto.getValueAt(linhaSelecionada, 3).toString();

            objALterar = new Computador(id, hd, processador);

            // //Redirecionar tela de cadastro
            TabCadastar.setSelectedIndex(0);
            TextProcessador.setText(String.valueOf(objALterar.getProcessador()));
            TextHD.setText(String.valueOf(objALterar.getHd()));

        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "Selecione um linha da tabela ");
        }
    }// GEN-LAST:event_BtnAlterarActionPerformed

