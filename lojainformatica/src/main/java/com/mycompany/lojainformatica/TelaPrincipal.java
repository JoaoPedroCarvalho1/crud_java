    private void BtnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_BtnCadastrarActionPerformed

        if (objALterar != null) {
            // alteração

            String hd = TextHD.getText();
            String processador = TextProcessador.getText();

            objALterar.setHd(hd);
            objALterar.setProcessador(processador);

            // chamar banco
            boolean retornoAlteração = ComputadorDAO.Alterar(objALterar);

            if (retornoAlteração) {
                JOptionPane.showMessageDialog(rootPane, "Sucesso ao Alterar");

                // limpar campos

                objALterar = null;
                TextProcessador.setText("");
                TextHD.setText("");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Erro ao Alterar");
            }

        } else {
            // inclusão
            String hd = TextHD.getText();
            String processador = TextProcessador.getText();

            Computador objCadastrar = new Computador(hd, processador);
            boolean retornoBanco = ComputadorDAO.salvar(objCadastrar);

            if (retornoBanco) {
                JOptionPane.showMessageDialog(rootPane, "Cadastrado com Sucesso!");
                TextProcessador.setText("");
                TextHD.setText("");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Falha ao cadastrar");
            }

        }
    }// GEN-LAST:event_BtnCadastrarActionPerformed

