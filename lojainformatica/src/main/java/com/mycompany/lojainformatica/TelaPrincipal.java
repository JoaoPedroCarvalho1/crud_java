    private void BtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_BtnExcluirActionPerformed
        int linhaSelecionada = TblProdutos.getSelectedRow();

        DefaultTableModel modeloTabela = (DefaultTableModel) TblProdutos.getModel();
        int idExcluir = Integer.parseInt(modeloTabela.getValueAt(linhaSelecionada, 0).toString());
        boolean retorno = ComputadorDAO.Excluir(idExcluir);
        if (retorno) {
            JOptionPane.showMessageDialog(rootPane, "Excluido com sucesso");
            AtualizarTabela();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Erro ao excluir");
        }
    }// GEN-LAST:event_BtnExcluirActionPerformed

    public void AtualizarTabela() {
        ArrayList<Computador> lista = ComputadorDAO.listar();

        DefaultTableModel modelotabela = (DefaultTableModel) TblProdutos.getModel();

        modelotabela.setRowCount(0);

        for (Computador item : lista) {
            modelotabela.addRow(new String[] {
                    String.valueOf(item.getId()),
                    item.getMarca(),
                    item.getHd(),
                    item.getProcessador()
            });
        }
    }

